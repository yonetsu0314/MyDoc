/*----------------------------------------------------------------------*/
/*	DEL_FLG								*/
/*----------------------------------------------------------------------*/
/* 説明                                                  		*/
/*	イベントフラグの削除。イベントフラグのアセスアドレスをイベント	*/
/*	フラグＩＤとしてシステムコールを行う。システムコールの戻り値（	*/
/*	エラーコード）をコンバートして返す。				*/
/*----------------------------------------------------------------------*/
/* 引き数								*/
/*	flgaa	: イベントフラグのアクセスアドレス			*/
/*									*/
/* 戻り値								*/
/*	int	: 0	･･･ 正常終了					*/
/*		  -n	･･･ error code					*/
/*----------------------------------------------------------------------*/
int
DEL_FLG(
	unsigned int	flgaa
	)
{
	return( err_code( del_flg( (ID)flgaa)));
}
