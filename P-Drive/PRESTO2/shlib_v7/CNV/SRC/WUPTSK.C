#include	"errcode.h"


/*----------------------------------------------------------------------*/
/*	WUP_TSK								*/
/*----------------------------------------------------------------------*/
/* 説明                                                  		*/
/*	タスクの起床。タスクのアセスアドレスをタスクＩＤとしてシステム	*/
/*	コールを行う。システムコールの戻り値（エラーコード）をコンバー	*/
/*	トして返す。							*/
/*----------------------------------------------------------------------*/
/* 引き数								*/
/*	tskaa	: タスクのアクセスアドレス				*/
/*									*/
/* 戻り値								*/
/*	int	: 0	･･･ 正常終了					*/
/*		  -n	･･･ error code					*/
/*----------------------------------------------------------------------*/
int
WUP_TSK(
	unsigned int	tskaa
	)
{
	if(!tskaa)	return(0x05);	/* rx116との互換のため、エラーを返す*/
					/* NORTi では、ID 0で自タスクを起床する*/
	return( err_code( wup_tsk( (ID)tskaa)));
}
