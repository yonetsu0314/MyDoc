/*----------------------------------------------------------------------*/
/*	DEL_TSK								*/
/*----------------------------------------------------------------------*/
/* 説明                                                  		*/
/*	タスクの削除。タスクのアセスアドレスをタスクＩＤとしてシステム	*/
/*	コールを行う。システムコールの戻り値（エラーコード）をコンバー	*/
/*	トして返す。							*/
/*----------------------------------------------------------------------*/
/* 引き数								*/
/*	tskaa	: タスクのアクセスアドレス				*/
/*									*/
/* 戻り値								*/
/*	int	: 0	･･･ 正常終了					*/
/*		  -n	･･･ error code					*/
/*----------------------------------------------------------------------*/
int
DEL_TSK(
	unsigned int	tskaa
	)
{
	return( err_code( del_tsk( (ID)tskaa)));
}
