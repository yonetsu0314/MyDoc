/* 説明                                                  		*/
/*	buffで示されるバッファの指定バイト数をＥＸＯＲし、その		*/
/*	結果を返す。							*/
/*----------------------------------------------------------------------*/
/* 引き数								*/
/*	*buff		: ﾊﾞｯﾌｧへのﾎﾟｲﾝﾀ				*/
/*	len		: ﾊﾞｲﾄ数					*/
/*									*/
/* 戻り値								*/
/*	unsigned char	: 計算結果					*/
/*----------------------------------------------------------------------*/

unsigned char
check_bcc(
	unsigned char	*buff,
	int		len
	)
{
	unsigned char	bcc = (unsigned char)0;

	while( len-- ) {
		bcc ^= *buff;
		++buff;
	}
	return(bcc);
}
