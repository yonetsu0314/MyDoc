/*----------------------------------------------------------------------*/
/* 説明					  				*/
/*	ＡＴＡ偏差のテキストを構造体にセーブする。		    　　*/
/*----------------------------------------------------------------------*/
/* 引き数	      							*/
/*	*buf	: 受信ﾃｷｽﾄ･ﾊﾞｯﾌｧへのﾎﾟｲﾝﾀ		    		*/
