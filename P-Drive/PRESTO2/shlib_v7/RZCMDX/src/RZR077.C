int
rz_scan_ata_dev (
	struct fmt_ata_dev	*buf
	)
{
	rzr077s.ch_id	= ascii_hex( buf->ch_id);
	rzr077s.ata_dev	= htol( (char *)buf->ata_dev);

	return(0);
}
