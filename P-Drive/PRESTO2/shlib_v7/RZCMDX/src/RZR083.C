/*----------------------------------------------------------------------*/
/*	rz_scan_shift_mon						*/
/*----------------------------------------------------------------------*/
/* 説明					  				*/
/*	シフトデータ参照（要求）仕様を構造体にセーブする。		*/
/*----------------------------------------------------------------------*/
/* 引き数	      							*/
/*	*buf	: 受信ﾃｷｽﾄ･ﾊﾞｯﾌｧへのﾎﾟｲﾝﾀ		    		*/
/*									*/
/* 戻り値								*/
/*	int	: 0	･･･ 正常終了					*/
/*----------------------------------------------------------------------*/

int		
rz_scan_shift_mon( 
	struct fmt_shift_mon	*buf
	)
{
	rzr083s.ch_id 		= ascii_hex( buf->ch_id );
	rzr083s.req_type 	= ascii_hex( buf->req_type );
	rzr083s.data_page 	= htoc( (char *)buf->data_page );
	return( 0 );
}
