/*		  -1	･･･ エラー					*/
/*----------------------------------------------------------------------*/

int
pr_scan_mlt_ext (
	struct fmt_mlt_ext	*buf
	)
{
	prr0095s.s_cmd	= ascii_hex( buf->s_cmd);
	prr0095s.p1	= htoc( (char *)buf->p1);
	prr0095s.p2	= htoc( (char *)buf->p2);
	prr0095s.p3	= htoc( (char *)buf->p3);
	prr0095s.p4	= htoc( (char *)buf->p4);

	return(0);
}
