/*----------------------------------------------------------------------*/
/* 	pr_scan_mlt_ext							*/
/*----------------------------------------------------------------------*/
/* 説明					  				*/
/*	マルチ連動拡張パラメータのテキストを構造体にセーブする。   　　	*/
/*----------------------------------------------------------------------*/
/* 引き数	      							*/
/*	*buf	: 受信ﾃｷｽﾄ･ﾊﾞｯﾌｧへのﾎﾟｲﾝﾀ		    		*/
/*									*/
/* 戻り値								*/
