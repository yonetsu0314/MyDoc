/*----------------------------------------------------------------------*/
/*	pr_print_shift_mon						*/
/*----------------------------------------------------------------------*/
/* 説明					  				*/
/*	シフトデータ参照（要求）仕様を構造体にセーブする。		*/
/*----------------------------------------------------------------------*/
/* 引き数	      							*/
/*	*txt	: 送信テキスト・バッファへのポインタ    		*/
/*	*pkt	: パラメータ・パケットへのポインタ   			*/
/*									*/
/* 戻り値								*/
/*	int	: buf+sizeof(fmt_mch_construct)	･･･ 正常終了		*/
/*----------------------------------------------------------------------*/

void *
pr_print_shift_mon(
	struct fmt_shift_mon	*txt,
	struct prr0083t		*pkt
	)
{
	txt->ch_id 	= hex_ascii(pkt->ch_id);
	txt->req_type 	= hex_ascii(pkt->req_type);
	ctoh( pkt->data_page, (char *)txt->data_page );
	return(++txt);
}
