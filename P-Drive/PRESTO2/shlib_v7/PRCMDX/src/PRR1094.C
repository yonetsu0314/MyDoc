/*----------------------------------------------------------------------*/
/*	pr_print_mlt_parm2_ext						*/
/*----------------------------------------------------------------------*/
/* 説明					  				*/
/*	補正方向構造体データをテキストに変換する。			*/
/*----------------------------------------------------------------------*/
/* 引き数	      							*/
/*	*txt	: 送信ﾃｷｽﾄ･ﾊﾞｯﾌｧへのﾎﾟｲﾝﾀ(char *)	    		*/
/*	*pkt	: ﾊﾟﾗﾒｰﾀ･ﾊﾟｹｯﾄへのﾎﾟｲﾝﾀ		    			*/
/*									*/
/* 戻り値								*/
/*	int	: txt+sizeof(fmt_prec_dump)	･･･ 正常終了		*/
/*		  NULL				･･･ エラー		*/
/*----------------------------------------------------------------------*/

void *
pr_print_mlt_parm2_ext(
	struct fmt_mlt_parm2_ext	*txt,
	struct prr1093t			*pkt
	)
{
	BYTE		*buf;
	int		i, j;

	ctoh(pkt->nof_mch, (char *)&txt->nof_mch);

	buf = (PTR)txt->node_id[0];
	for(i = pkt->nof_mch, j = 0 ; i ; i--, j++) {
		ctoh(pkt->node_id[j], (char *)buf);
		buf += sizeof(txt->node_id[0]);
	}
	return buf;
}
