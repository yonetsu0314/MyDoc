int
rz_scan_req_netmap(
	struct fmt_req_netmap	*buf
	)
{
	rzr071s.brd_id = ascii_hex(buf->brd_id);
	rzr071s.dev_no = htoc((char *)buf->dev_no);
	return(0);
}
