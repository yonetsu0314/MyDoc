/*	rz_scan_req_netmap						*/
/*----------------------------------------------------------------------*/
/* 説明					  				*/
/*	ネットワーク参加状態の要求テキストを構造体にセーブする。	*/
/*----------------------------------------------------------------------*/
/* 引き数	      							*/
/*	*buf	: 受信ﾃｷｽﾄ･ﾊﾞｯﾌｧへのﾎﾟｲﾝﾀ		    		*/
/*									*/
