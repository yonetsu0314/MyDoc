/*----------------------------------------------------------------------*/
/* 説明					  				*/
/*	ネットワーク参加状態要求の構造体データをテキストに変換する。	*/
/*----------------------------------------------------------------------*/
/* 引き数	      							*/
/*	*txt	: 送信ﾃｷｽﾄ･ﾊﾞｯﾌｧへのﾎﾟｲﾝﾀ(char *)	    		*/
/*	*pkt	: ﾊﾟﾗﾒｰﾀ･ﾊﾟｹｯﾄへのﾎﾟｲﾝﾀ		    			*/
/*									*/
/* 戻り値								*/
/*	int	: txt+sizeof(fmt_req_netmap)	･･･ 正常終了		*/
/*		  NULL				･･･ エラー		*/
/*----------------------------------------------------------------------*/

void *
rz_print_req_netmap(
	struct fmt_req_netmap	*txt,
	struct rzr071t		*pkt
	)
{
	txt->brd_id = hex_ascii(pkt->brd_id);
	ctoh(pkt->dev_no, (char *)&txt->dev_no);
	return(++txt);
}
