/*----------------------------------------------------------------------*/
/*	rz_scan_drain_run						*/
/*----------------------------------------------------------------------*/
/* 説明					  				*/
/*	全排出開始テキストを構造体にセーブする。			*/
/*----------------------------------------------------------------------*/
/* 引き数	      							*/
/*	*buf	: 受信ﾃｷｽﾄ･ﾊﾞｯﾌｧへのﾎﾟｲﾝﾀ		    		*/
/*									*/
/* 戻り値								*/
