int
rz_scan_drain_run(
	struct fmt_drain_run	*buf
	)
{
	rzr041s.ch_id = ascii_hex(buf->ch_id);
	rzr041s.infeed = ascii_hex(buf->infeed);
	rzr041s.auto_zero = ascii_hex(buf->auto_zero);
	return(0);
}
