#include	<stddef.h>			/* ANSI std		*/
#include	<rom\fcntl.h>
#include	<rom\handle.h>
#include	<drive\iofsys.h>

int
read(
	int		fd,
	char		*buff,
	int		len
	)
{
	int		ch, i;

	ch = fd & 0xff;
	if(((i= ((fd & 0xff00) >> 8)-1) < 0) || (i > __KEY_MAX_-1) ||
	  (iodrv_read[i] == NULL))				return(-1);
	return(iodrv_read[i](ch, (unsigned char *)buff, len));
}
