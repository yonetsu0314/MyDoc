#include	<stddef.h>			/* ANSI std		*/
#include	<rom\fcntl.h>
#include	<rom\handle.h>
#include	<drive\iofsys.h>

int
seek(
	int		fd,
	int		offset,
	int		origin
	)
{
	int		ch, i;

	ch = fd & 0xff;
	if(((i= ((fd & 0xff00) >> 8)-1) < 0) || (i >= __FILE_KEY_MAX_) ||
	  (iodrv_seek[i] == NULL))				return(-1);
	return(iodrv_seek[i](ch, offset, origin));
}
