/*----------------------------------------------------------------------*/
/*	pr_print_drain_run						*/
/*----------------------------------------------------------------------*/
/* 説明									*/
/*	全排出開始構造体のデータをテキストに変換する。			*/
/*----------------------------------------------------------------------*/
/* 引き数								*/
/*	*txt	: 送信ﾃｷｽﾄ･ﾊﾞｯﾌｧへのﾎﾟｲﾝﾀ(char *)			*/
/*	*pkt	: ﾊﾟﾗﾒｰﾀ･ﾊﾟｹｯﾄへのﾎﾟｲﾝﾀ					*/
/*									*/
/* 戻り値								*/
/*	int	: txt+sizeof(fmt_drain_run)	･･･ 正常終了		*/
/*		  NULL				･･･ エラー		*/
/*----------------------------------------------------------------------*/

void *
pr_print_drain_run(
	struct fmt_drain_run	*txt,
	struct prr0041t		*pkt
	)
{
	txt->ch_id = hex_ascii(pkt->ch_id);
	ltoh(pkt->head_ptn, (char *)txt->head_ptn );
	return(++txt);
}
