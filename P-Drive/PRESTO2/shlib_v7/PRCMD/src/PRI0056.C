/*----------------------------------------------------------------------*/
/*	pr_print_ryin							*/
/*----------------------------------------------------------------------*/
/* 説明									*/
/*	リレー基板信号入力状態応答データをテキストに変換する。		*/
/*----------------------------------------------------------------------*/
/* 引き数								*/
/*	*txt	: 送信ﾃｷｽﾄ･ﾊﾞｯﾌｧへのﾎﾟｲﾝﾀ(char *)			*/
/*	*pkt	: ﾊﾟﾗﾒｰﾀ･ﾊﾟｹｯﾄへのﾎﾟｲﾝﾀ					*/
/*									*/
/* 戻り値								*/
/*	int	: txt+sizeof(fmt_ryin)		･･･ 正常終了		*/
/*		  NULL				･･･ エラー		*/
/*----------------------------------------------------------------------*/

void *
pr_print_ryin(
	struct fmt_ryin	*txt,
	struct pri0055t	*pkt
	)
{
	txt->brd_no = hex_ascii( pkt->brd_no );
	ctoh(pkt->input_ptn, (char *)txt->input_ptn);
	return(++txt);
}
