/*----------------------------------------------------------------------*/
/*	pr_print_echo							*/
/*----------------------------------------------------------------------*/
/* 説明									*/
/*	受信完了構造体のデータをテキストに変換する。			*/
/*----------------------------------------------------------------------*/
/* 引き数								*/
/*	*txt	: 送信ﾃｷｽﾄ･ﾊﾞｯﾌｧへのﾎﾟｲﾝﾀ(char *)			*/
/*	*pkt	: ﾊﾟﾗﾒｰﾀ･ﾊﾟｹｯﾄへのﾎﾟｲﾝﾀ					*/
/*									*/
/* 戻り値								*/
/*	int	: txt+sizeof(fmt_echo)	･･･ 正常終了			*/
/*		  NULL			･･･ エラー			*/
/*----------------------------------------------------------------------*/

void *
pr_print_echo(
	struct fmt_echo	*txt,
	struct pri0019t	*pkt
	)
{
	txt->command = pkt->command;
	txt->ch_id = hex_ascii(pkt->ch_id);
	return(++txt);
}
