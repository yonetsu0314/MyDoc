int
m66230_sys_init(
	int						ch_no,
	const struct m66230pk	*pkt,
	union aap				*rx_tsk
	)
{
	unsigned short	buf_max,		/* 最大ﾊﾞｯﾌｧ･ｻｲｽﾞ			*/
					mask;			/* mask bit pattern			*/
	int				*phy_inf;		/* & to physical info		*/
	int				err;

	if(ch_no >= M66230_CH_MAX)								return(-1);
	m66230_ds.ifp = (int *)&m66230_ds.acadr;
	if((pkt->siz_txbuf == 0)||(pkt->siz_rxbuf == 0))		return(-1);
	if((m66230_rx_tsk[ch_no] = rx_tsk) == NULL)				return(-1);
	if(err = init_m66230_rx(ch_no))							return(-1);
	if(err = init_m66230_tx(ch_no))							return(-1);
	for(buf_max= (unsigned short)(pkt->siz_txbuf-1), 
		mask =(unsigned short)0x8000; mask; mask >>= 1) {
		if(buf_max & mask) {
			while(mask >>= 1)  buf_max |= mask;
			if((m66230tx[ch_no].buff = malloc((size_t)(buf_max+1)))
											== NULL)		return(-1);
			m66230tx[ch_no].buf_max = buf_max;
		}
	}
	for(buf_max= (unsigned short)(pkt->siz_txbuf-1),
		mask =(unsigned short)0x8000; mask; mask >>= 1) {
		if(buf_max & mask) {
			while(mask >>= 1)  buf_max |= mask;
			if((m66230rx[ch_no].buff = malloc((size_t)(buf_max+1)))
											== NULL)		return(-1);
			m66230rx[ch_no].buf_max = buf_max;
		}
	}
	if((phy_inf = _m66230_init(ch_no, pkt->stp_len,	pkt->pari_sel,
									 pkt->baud)) == NULL)	return(-1);
	m66230_ctxt_tbl[ch_no].io_base = *phy_inf;
	iodrv_open[__MPSC_-1] = m66230_open;
	return(0);
}
