/*** 変更履歴 *****************************************************************/
/******************************************************************************/

#include "fsbase.h"
#include "os.h"
#include "fs_date.h"

F_VOID FMR_osGetTime(
    F_WORD * setday,                    /* 取得年月日設定領域                 */
    F_WORD * settime,                   /* 取得時分秒設定領域                 */
    F_BYTE * setmtime                   /* 取得ミリ秒設定領域                 */
)
{
	DATE_INFO date;

	fs_get_date(&date);
	fs_get_time(&date);

	*( settime ) = (F_WORD)(date.second / 2);
	*( settime ) |= (F_WORD)(date.minute << 5);
	*( settime ) |= (F_WORD)(date.hour << 11);

	*( setday ) = (F_WORD)date.day;
	*( setday ) |= (F_WORD)(date.month << 5);
	*( setday ) |= (F_WORD)((date.year - 60) << 9);
	
	if( setmtime != NULL )
	{
		*( setmtime ) = 0;	/* 取得ミリ秒はOとする           */
	}
}

