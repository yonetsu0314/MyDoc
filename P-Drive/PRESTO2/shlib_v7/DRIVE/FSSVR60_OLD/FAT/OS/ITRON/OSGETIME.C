/* 関 数 名：FMR_osGetTime                                                    */
/*         ：                                                                 */
/* パラメタ：OUT: F_WORD * 取得年月日設定領域                                 */
/*         ：     F_WORD * 取得時分秒設定領域                                 */
/*         ：     F_BYTE * 取得ミリ秒設定領域                                 */
/*         ：                                                                 */
/* 戻 り 値：NONE                                                             */
/*         ：                                                                 */
/* 概    要：OSから日付時刻を取得する                                         */
/*         ：127年12月31日23時59分58秒まで                                    */
/*         ：                                                                 */
/* 前提条件：①システム時刻開始(カウンタ0)を1985年01月01日00時00分00秒とする  */
/*         ：②グレゴリオ暦用                                                 */
/*         ：                                                                 */
/* 作 成 日：2004.05.26                                                       */
/* 作 成 者：T.Tsuneyoshi                                                     */
/*                                                                            */
