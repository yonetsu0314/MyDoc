#include "fsbase.h"
#include "os.h"

F_INT FMR_osSigSem(
    F_WORD semid                        /* セマフォID                         */
)
{
	F_INT l_rtn;                        /* OSシステムコール戻り値             */
	
	l_rtn = sig_sem(
		semid
		);
	if( l_rtn != DFMR_OK )
	{
		l_rtn = DFMR_SIGSENERR;
	}
	return l_rtn;
}
