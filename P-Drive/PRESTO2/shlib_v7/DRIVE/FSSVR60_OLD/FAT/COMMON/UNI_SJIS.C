#include "fsbase.h"

F_VOID FMR_exchgUnicodeToSJIS(
	F_WORD * srcstr,                    /* 変換前文字列                       */
	F_CHAR * buff,                      /* 変換後文字列                       */
	F_WORD num                          /* 変換文字数                         */
)
{
	F_WORD l_cnt;                       /* ループカウンタ                     */
	F_WORD l_work;                      /* ワークエリア                       */
	
	for( l_cnt = 0; l_cnt < num; l_cnt ++ )
	{
#ifdef DFMR_ENDIAN_BIG
		l_work = ( F_WORD )( *( srcstr + l_cnt ) & 0xFF00 );
		l_work = ( F_WORD )( l_work >> 8 );
#else /* DFMR_ENDIAN_BIG */
		l_work = ( F_WORD )( *( srcstr + l_cnt ) & 0x00FF );
#endif /* DFMR_ENDIAN_BIG */
		*( buff + l_cnt ) = ( F_CHAR )l_work;
	}
}
