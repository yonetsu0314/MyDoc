/* 関 数 名：FMR_exchgUnicodeToSJIS                                           */
/*         ：                                                                 */
/* パラメタ：IN:  F_WORD * 変換前文字列                                       */
/*         ：     F_CHAR 変換文字数                                           */
/*         ：                                                                 */
/*         ：OUT: F_WORD * 変換後文字列                                       */
/*         ：                                                                 */
/*         ：                                                                 */
/* 戻 り 値：void                                                             */
/*         ：                                                                 */
/* 概    要：Unicode → ASCII/Shift-JISの変換&小文字→大文字の変換を行う。    */
/*         ：                                                                 */
/* 作 成 日：2004.06.90                                                       */
/* 作 成 者：T.Tsuneyoshi                                                     */
/*                                                                            */
