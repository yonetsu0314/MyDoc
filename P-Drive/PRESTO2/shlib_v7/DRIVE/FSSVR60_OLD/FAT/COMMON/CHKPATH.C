/*         ：                                                                 */
/* パラメタ：IN:  F_CHAR * パス名                                             */
/*         ：                                                                 */
/*         ：OUT: F_CHAR * 論理ドライブ番号                                   */
/*         ：                                                                 */
/*         ：                                                                 */
/* 戻 り 値：正常終了  DFMR_SUC                                               */
/*         ：異常終了  DFMR_ERRPARAM                                          */
/*         ：          DFMR_DRVNOCONNECTERR                                   */
/*         ：                                                                 */
/* 概    要：パスサイズのチェック及び論理ドライブ接続チェック                 */
/*         ：                                                                 */
