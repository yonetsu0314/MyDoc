#include "fsbase.h"

F_INT FMR_chkPathName ( 
    F_CHAR * path,                      /* パス名                             */
    F_CHAR * drvnum                     /* 論理ドライブ番号                   */
)
{
	F_WORD  l_cnt;                      /* 汎用カウンタ                       */
	F_CHAR  l_drvnum;                   /* 論理ドライブ番号                   */

	/**************************************************************************/
	/* パス名チェック                                                         */
	/**************************************************************************/
	if( path == NULL )
	{
		/* パス名がNULLの場合 */
		return DFMR_ERRPARAM;           /* パラメータエラー返却               */
	}
	/**************************************************************************/
	/* 論理ドライブ番号チェック                                               */
	/**************************************************************************/
	l_drvnum = *( path );               /* 論理ドライブ番号取得               */
	l_drvnum &=  0xDF;
	if( ( *( path + 1 ) != ':' )  ||
		( *( path + 2 ) != '\\' ) ||
		( *( path + 3 ) == NULL ) ||
		( ( l_drvnum < DFMR_TOPDRVNUM ) || ( DFMR_ENDDRVNUM < l_drvnum ) ) )
	{
		return DFMR_ERRPARAM;
	}
	if( MFMR_DRVINFO( l_drvnum ) == ( F_VOID * )DFMR_RAMINIT )
	{
		/* 論理ドライブが未接続の場合 */
		return DFMR_DRVNOCONNECTERR;    /* 論理ドライブ未接続エラー           */
	}
	*( drvnum ) = l_drvnum;             /* 論理ドライブ番号返却               */
	return DFMR_OK;                     /* 正常終了                           */
}
