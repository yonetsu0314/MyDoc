#include "fsbase.h"

long FMR_Seek ( 
	int filehandle,                     /* ファイルハンドル                   */
	long offset,                        /* オフセット                         */
	int origin                          /* 初期位置                           */
)
{
	F_INT l_rtn;                        /* サブルーチン戻り値                 */
	F_INT l_ortn;                       /* OS部関数戻り値                     */
	F_CHAR l_drvnum;                    /* 論理ドライブ番号                   */
	
	/**************************************************************************/
	/* パラメータチェックを行う                                               */
	/**************************************************************************/
	/* ファイルハンドル確認 */
	if( ( filehandle < 0 ) || ( DFMR_USR_MAXFILEHANDLE <= filehandle ) )
	{
		/* ファイルハンドル番号が許容範囲外の場合 */
		return DFMR_EBADF;
	}
	
	if( tfmr_filehandle[ filehandle ].direntry.name[ 0 ] == DFMR_INITBYTE )
	{
		/* ファイルハンドルにファイルが関連付けされていない場合 */
		return DFMR_EBADF;
	}
	
	/* 初期位置の確認 */
	switch( origin )
	{
	case DFMR_SEEK_SET:
	case DFMR_SEEK_CUR:
	case DFMR_SEEK_END:
		break;
	default:
		return DFMR_ERRPARAM;
	}
	
	/**************************************************************************/
	/* 初期設定を行う                                                         */
	/**************************************************************************/
	l_drvnum = tfmr_filehandle[ filehandle ].drivenum;
	                                    /* 論理ドライブ番号取得               */
	if( MFMR_DRVINFO( l_drvnum ) == ( F_VOID * )DFMR_RAMINIT )
	{
		/* 論理ドライブが未接続の場合 */
		return DFMR_DRVNOCONNECTERR;    /* 論理ドライブ未接続エラー           */
	}
	/**************************************************************************/
	/* セマフォ資源獲得                                                       */
	/**************************************************************************/
	l_ortn =  FMR_osWaiSem(
		MFMR_SEMID( l_drvnum )
		);
	if( l_ortn != DFMR_OK )
	{
		return l_ortn;
	}
	
	/**************************************************************************/
	/* ポインタ移動処理を行う                                                 */
	/**************************************************************************/
	l_rtn = FMR_setPointer(
		filehandle,                     /* ファイルハンドル                   */
		offset,                         /* オフセット                         */
		origin                          /* 初期位置                           */
		);
	if( l_rtn != DFMR_OK )
	{
		l_ortn =  FMR_osSigSem(
			MFMR_SEMID( l_drvnum )
			);
		if( l_ortn != DFMR_OK )
		{
			return l_ortn;
		}
		return ( F_LONG )l_rtn;
	}
	
	/**************************************************************************/
	/* セマフォ資源返却                                                       */
	/**************************************************************************/
	l_ortn =  FMR_osSigSem(
		MFMR_SEMID( l_drvnum )
		);
	if( l_ortn != DFMR_OK )
	{
		return l_ortn;
	}
	
	/**************************************************************************/
	/* ファイルポインタを返却                                                 */
	/**************************************************************************/
	return ( F_LONG )tfmr_filehandle[ filehandle ].fptr;
}
