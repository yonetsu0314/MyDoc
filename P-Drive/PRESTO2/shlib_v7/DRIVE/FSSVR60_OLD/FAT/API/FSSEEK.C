/*         ：                                                                 */
/*         ：                                                                 */
/* 戻 り 値：正常終了  移動後のファイルポインタ位置                           */
/*         ：異常終了  DFMR_EBADF                                             */
/*         ：          DFMR_ERRPARAM                                          */
/*         ：                                                                 */
/* 概    要：ファイルポインタの移動                                           */
