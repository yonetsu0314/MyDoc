#include "fsbase.h"

int FMR_Close ( 
    int filehandle                      /* ファイルハンドル番号               */
)
{
	F_INT l_ortn;                       /* OS部戻り値                         */

	F_BYTE l_drvnum;                    /* 論理ドライブ番号                   */

	/**************************************************************************/
	/* パラメータチェック                                                     */
	/**************************************************************************/
	/* ファイルハンドルの確認 */
	if( ( filehandle < 0 ) || ( DFMR_USR_MAXFILEHANDLE <= filehandle ) )
	{
		/* ファイルハンドルが範囲外の場合 */
		return DFMR_ERRPARAM;
	}
	
	if( tfmr_filehandle[ filehandle ].direntry.name[ 0 ] == DFMR_INITBYTE )
	{
		/* ファイルハンドルにファイルが関連付けされていない場合 */
		return DFMR_EBADF;
	}
	/* 論理ドライブ番号取得 */
	l_drvnum = tfmr_filehandle[ filehandle ].drivenum;
	/**************************************************************************/
	/* セマフォ資源獲得                                                       */
	/**************************************************************************/
	l_ortn = FMR_osWaiSem(
		MFMR_SEMID( l_drvnum )
		);
	if( l_ortn != DFMR_OK )
	{
		return l_ortn;
	}
	/**************************************************************************/
	/* ファイルハンドル初期化                                                 */
	/**************************************************************************/
	FMR_initFileHandle(
		filehandle                      /* ファイルハンドル番号               */
		);
	/**************************************************************************/
	/* セマフォ資源返却                                                       */
	/**************************************************************************/
	l_ortn = FMR_osSigSem(
		MFMR_SEMID( l_drvnum )
		);
	if( l_ortn != DFMR_OK )
	{
		return l_ortn;
	}
	/**************************************************************************/
	/* 正常終了返却                                                           */
	/**************************************************************************/
	return DFMR_SUC;
}
