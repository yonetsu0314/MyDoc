/* 関 数 名：FMR_framBitOff                                                   */
/*         ：                                                                 */
/* パラメタ：IN:  F_DWORD クラスタ番号                                        */
/*         ：     F_BYTE * クラスタ使用状況展開領域                           */
/*         ：                                                                 */
/* 戻 り 値：なし                                                             */
/*         ：                                                                 */
/* 概    要：指定クラスタに対応するクラスタ使用状況展開領域のBitをOFFにする   */
/*         ：                                                                 */
/* 作 成 日：2004.09.02                                                       */
/* 作 成 者：T.Tsuneyoshi                                                     */
/*                                                                            */
/******************************************************************************/
/*** 変更履歴 *****************************************************************/
