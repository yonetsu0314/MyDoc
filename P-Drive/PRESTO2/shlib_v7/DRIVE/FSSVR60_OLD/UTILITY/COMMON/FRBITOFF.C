#include "fsbase.h"
#include "utility.h"

F_VOID FMR_framBitOff(
	F_DWORD clstnum,                    /* クラスタ番号                       */
	F_BYTE * ramaddr                    /* クラスタ使用状況展開領域           */
)
{
	F_BYTE l_readbuf;                   /* 対象Byte読込み領域                 */
	F_BYTE l_bitpos;                    /* 対象Bit位置                        */
	
	/* クラスタ使用状況展開領域の対象のBitが存在する領域を取得する */
	l_readbuf = *( ramaddr + sizeof( TFMR_SDWORKAREA ) 
		+ ( clstnum >> DFMR_SD_BITONCHAR ) );
	
	/* クラスタ番号より対象Bit位置を取得する */
	l_bitpos = clstnum & DFMR_SD_BITPOS;
	
	/* 対象のBitをOFFにする */
	l_readbuf &= ( F_CHAR )( ~( 1 << l_bitpos ) );
	
	/* クラスタ使用状況展開領域を更新する */
	*( ramaddr + sizeof( TFMR_SDWORKAREA ) 
		+ ( clstnum >> DFMR_SD_BITONCHAR ) ) = l_readbuf;
}
