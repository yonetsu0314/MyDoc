#include "ram_def.h"

/****************************************************************************/
/* 関 数 名：RAM_SubMemSet                                                  */
/*         ：                                                               */
/* パラメタ：IN:    F_BYTE     * dest           設定先ポインタ              */
/*         ：       F_BYTE       code           設定コード                  */
/*                  F_WORD       count          設定バイト数                */
/*         ：                                                               */
/* 戻 り 値：なし                                                           */
/*         ：                                                               */
/* 概    要：設定先へ設定コードを設定する。                                 */
/*         ：                                                               */
/* 作 成 日：2003.02.19                                                     */
/* 作 成 者：                                                               */
/*                                                                          */
/****************************************************************************/
/*** 変更履歴 ***************************************************************/
/*  履歴               変    更    内    容                日 付   修 正 者 */
/****************************************************************************/

void  RAM_SubMemSet(	F_BYTE	  * dest,				/* 設定先ポインタ			*/
						F_BYTE		code,				/* 設定コード				*/
						F_WORD		count )				/* 設定バイト数             */
{
	F_WORD		cnt;		/* ループカウンタ	*/

	for ( cnt = 0; cnt < count; cnt++ )
	{
		*dest = code;                              		/* コード設定				*/
		dest++;
	}
}

/****************************************************************************/
/* 関 数 名：RAM_SubMemCpy                                                  */
/*         ：                                                               */
/* パラメタ：IN:    F_BYTE     * dest           コピー先ポインタ            */
/*         ：       F_BYTE     * src            コピー元ポインタ            */
/*                  F_WORD       count          コピーバイト数              */
/*         ：                                                               */
/* 戻 り 値：なし                                                           */
/*         ：                                                               */
/* 概    要：コピー元からコピー先へコピーする。                             */
/*         ：                                                               */
/* 作 成 日：2003.02.19                                                     */
/* 作 成 者：                                                               */
/*                                                                          */
/****************************************************************************/
/*** 変更履歴 ***************************************************************/
/*  履歴               変    更    内    容                日 付   修 正 者 */
/****************************************************************************/

void  RAM_SubMemCpy(	F_BYTE	  * dest,				/* 設定先ポインタ			*/
						F_BYTE	  * src,				/* 設定コード				*/
						F_WORD		count )				/* 設定バイト数             */
{
	F_WORD		cnt;		/* ループカウンタ	*/

	for ( cnt = 0; cnt < count; cnt++ )
	{
		*dest = *src;									/* コピー					*/
		dest++;
		src++;
    }

}

