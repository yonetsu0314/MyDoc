/* 関 数 名：FMR_framBitChk                                                   */
/*         ：                                                                 */
/* パラメタ：IN:  F_DWORD クラスタ番号                                        */
/*         ：     F_BYTE * クラスタ使用状況展開領域                           */
/*         ：                                                                 */
/* 戻 り 値：1 Bit ON(クラスタ使用状況未確認)                                 */
/*         ：0 Bit OFF(クラスタ使用状況確認済)                                */
/*         ：                                                                 */
/* 概    要：指定クラスタに対応するクラスタ使用状況のBitの確認を行う          */
/*         ：                                                                 */
/* 作 成 日：2004.09.02                                                       */
/* 作 成 者：T.Tsuneyoshi                                                     */
/*                                                                            */
/******************************************************************************/
/*** 変更履歴 *****************************************************************/
