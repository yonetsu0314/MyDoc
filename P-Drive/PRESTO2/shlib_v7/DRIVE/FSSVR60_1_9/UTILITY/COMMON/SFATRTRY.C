#include "fsbase.h"
#include "utility.h"

F_INT FMR_setFatSectorRetry(
	F_CHAR drvnum,                      /* ドライブ番号                       */
    F_BYTE fatarea                      /* FAT読込み領域番号                  */
)
{
	F_INT l_rtn;                        /* サブルーチン戻り値                 */
	F_INT l_retrycnt;                   /* リトライカウンタ                   */
	
	for( l_retrycnt = 0; l_retrycnt < DFMR_USR_SDRETRY; l_retrycnt ++ )
	{
		/* FATセクタを書込む */
		l_rtn = FMR_setFatSector(
			MFMR_DRVINFO( drvnum ),             /* 論理ドライブ構成情報       */
			MFMR_FATTABLE( drvnum, fatarea ),   /* FAT読込み領域              */
			fatarea                             /* FAT読込み領域番号          */
			);
		if( l_rtn == DFMR_OK )          /* 正常終了の場合は処理終了           */
		{
			break;
		}
		FMR_osRetryWait( );             /* ウェイト                           */
	}
	return l_rtn;
}
