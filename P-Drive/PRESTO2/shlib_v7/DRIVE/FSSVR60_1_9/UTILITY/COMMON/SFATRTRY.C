/*         ：     F_BYTE  FAT読込み領域番号                                   */
/*         ：                                                                 */
/* 戻 り 値：DFMR_SUC 正常終了                                                */
/*         ：(負の値) 異常終了                                                */
/*         ：                                                                 */
/* 概    要：対象FATセクタを書込む                                            */
/*         ：                                                                 */
