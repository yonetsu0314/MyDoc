/*         ：                                                                 */
/* パラメタ：IN:  F_CHAR * エントリの名称部                                   */
/*         ：     F_CHAR * 名称を設定する領域                                 */
/*         ：                                                                 */
/* 戻 り 値：ファイル名長                                                     */
/*         ：                                                                 */
/* 概    要：エントリの名称部よりショートファイル名を取得する                 */
/*         ：                                                                 */
/* 作 成 日：2004.09.02                                                       */
