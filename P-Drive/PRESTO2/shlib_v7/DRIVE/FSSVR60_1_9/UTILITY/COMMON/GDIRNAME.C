#include "fsbase.h"
#include "utility.h"

F_WORD FMR_getDirName(
	F_CHAR * entname,                   /* エントリの名称部                   */
	F_CHAR * pathname                   /* 名称を設定する領域                 */
)
{
	F_CHAR l_cnt;                       /* ループカウンタ                     */
	F_INT l_len;                        /* ファイル名長                       */
	
	/* デリミタを設定する */
	*( pathname ) = '\\';
	pathname ++;
	l_len = 1;                          /* デリミタ長分をファイル名長に設定   */
	
	/* ファイル名を設定する */
	for( l_cnt = 0; l_cnt < DFMR_FNAMESZ; l_cnt ++ )
	{
		if( *( entname + l_cnt ) == DFMR_SPACE )
		{
			break;
		}
		*( pathname ) = *( entname + l_cnt );
		pathname ++;
		l_len ++;                       /* ファイル名分をファイル名長に加算   */
	}
	if( *( entname + DFMR_FNAMESZ ) == DFMR_SPACE )
	{                                   /* 拡張子がない場合はここで終了       */
		return l_len;
	}
	/* ピリオドを設定する */
	*( pathname ) = '.';
	pathname ++;
	l_len ++;                           /* ピリオド分をファイル名長に加算     */
	
	/* 拡張子を設定する */
	entname += DFMR_FNAMESZ;
	for( l_cnt = 0; l_cnt < DFMR_EXTSZ; l_cnt ++ )
	{
		if( *( entname + l_cnt ) == DFMR_SPACE )
		{
			break;
		}
		*( pathname ) = *( entname + l_cnt );
		pathname ++;
		l_len ++;                       /* 拡張子分をファイル名長に加算       */
	}
	return l_len;
}
