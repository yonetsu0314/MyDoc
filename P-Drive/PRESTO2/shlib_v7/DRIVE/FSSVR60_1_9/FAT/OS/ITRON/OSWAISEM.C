#include "fsbase.h"
#include "os.h"

F_INT FMR_osWaiSem(
    F_WORD semid                        /* セマフォID                         */
)
{
	F_INT l_rtn;                        /* OSシステムコール戻り値             */
	
	l_rtn = wai_sem(
		semid
		);
	if( l_rtn != DFMR_OK )
	{
		l_rtn = DFMR_WAISENERR;
	}
	
	return l_rtn;
}
