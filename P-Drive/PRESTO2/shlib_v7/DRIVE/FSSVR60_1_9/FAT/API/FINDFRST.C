/*         ：                                                                 */
/* パラメタ：IN:  const char * ターゲットパス名                               */
/*         ：                                                                 */
/*         ：OUT: TFMR_FILE_INFO * ファイル状態状態格納領域                   */
/*         ：                                                                 */
/*         ：                                                                 */
/* 戻 り 値：正常終了  検索ハンドル                                           */
/*         ：異常終了  DFMR_ERRPARAM                                          */
/*                     DFMR_EMFILE                                            */
/*         ：                                                                 */
/* 概    要：ディレクトリ/ファイルの検索開始                                  */
