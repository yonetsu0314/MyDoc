#include "fsbase.h"

long FMR_FindFirst ( 
	const char * path,                  /* パス名                             */
	TFMR_FILE_INFO * fileinfo           /* ファイル状態格納領域               */
)
{
	F_LONG  l_srchhandle;               /* 取得検索ハンドル                   */
	F_DWORD l_work01;                   /* ワークエリア                       */
	F_DWORD l_cdirclsnum;               /* カレントディレクトリの             */
	                                    /* クラスタ番号                       */
    F_CHAR * l_pathlen;                 /* パス名先頭からの最終要素の位置     */

	TFMR_DIRENTRY l_entry;              /* 取得エントリ                       */
	TFMR_SEARCHINFO l_toppos;           /* 検索処理パラメータ:先頭位置        */
	TFMR_SEARCHINFO l_bottompos;        /* 検索処理パラメータ:最終位置        */

	F_INT l_ortn;                       /* OS部戻り値                         */
	F_INT l_rtn;                        /* サブルーチン戻り値                 */

	F_CHAR l_getname[ DFMR_MAXFILESZ ]; /* 取得ロングファイル名               */
	
	F_CHAR l_drvnum;                    /* 論理ドライブ番号                   */
	F_BYTE l_prsflg;                    /* 検索処理動作フラグ                 */
	F_BYTE l_namelen;                   /* 対象ファイル名長                   */

	/**************************************************************************/
	/* パラメータチェック                                                     */
	/**************************************************************************/
	l_rtn = FMR_chkPathName(
		( F_CHAR * )path,               /* パス名                             */
		&l_drvnum                       /* 論理ドライブ番号                   */
		);
	if( l_rtn != DFMR_OK )              /* エラー終了の場合                   */
	{
		return ( F_LONG )l_rtn;
	}
	/* ファイル状態格納領域のチェック */
	if( fileinfo == NULL )              /* ファイル状態格納領域がNULLの場合   */
	{
		return DFMR_ERRPARAM;           /* パラメータエラーを返却             */
	}
	/**************************************************************************/
	/* セマフォ資源獲得                                                       */
	/**************************************************************************/
	l_ortn = FMR_osWaiSem(
		MFMR_SEMID( l_drvnum )
		);
	if( l_ortn != DFMR_OK )
	{
		return ( F_LONG )l_ortn;
	}
	/**************************************************************************/
	/* カレントディレクトリ検索                                               */
	/**************************************************************************/
	/* 検索フラグ設定 */
	l_prsflg = DFMR_SRCHFLGWC;          /* ワイルドカード指定ON               */
	l_prsflg |= DFMR_SRCHFLGLFN;        /* ロングファイル名指定ON             */
	path += 3;                          /* 先頭の｢A:\｣を飛ばす                */
	
	/* カレントディレクトリ検索処理 */
	l_rtn = FMR_getCurrentDirCluster(
		l_drvnum,                       /* 論理ドライブ番号                   */
		( F_CHAR * )path,               /* パス名                             */
		&l_getname[ 0 ],                /* ロングファイル名用ワークエリア     */
		&l_prsflg,                      /* FMR_searchEntry用動作フラグ        */
		&l_toppos,                      /* 検索開始/終了先頭位置              */
		&l_pathlen,                     /* パス名先頭からの最終要素の位置     */
		&l_cdirclsnum                   /* カレントクラスタ番号               */
		);
	if( l_rtn < DFMR_OK )               /* エラー終了の場合                   */
	{
		goto ERROR;
	}
	l_namelen = ( F_BYTE )l_rtn;
	l_getname[ 0 ] = NULL;
	/**************************************************************************/
	/* 対象エントリ検索                                                       */
	/**************************************************************************/
	l_rtn = FMR_searchEntry(
		l_drvnum,                       /* 対象ドライブ番号                   */
		&l_toppos,                      /* 検索開始/終了先頭位置              */
		&l_bottompos,                   /* 検索終了最終位置                   */
		l_pathlen,                      /* 検索文字列                         */
		&l_entry,                       /* 取得エントリ                       */
		&l_getname[ 0 ],                /* 取得ロングファイル名               */
		l_namelen,                      /* 検索文字列長                       */
		l_prsflg                        /* 動作フラグ                         */
		);
	if( l_rtn != DFMR_OK )
	{
		if( l_rtn == DFMR_NOENTRY )
		{
			/* 対象エントリが存在しない場合、戻り値をDFMR_ENOENTとする    */
			l_rtn = DFMR_ENOENT;
		}
		goto ERROR;
	}
	/**************************************************************************/
	/* 検索ハンドル用セマフォ資源獲得                                         */
	/**************************************************************************/
	l_ortn = FMR_osWaiSem(
		g_semid
		);
	if( l_ortn != DFMR_OK )
	{
		l_rtn = l_ortn;
		goto ERROR;
	}
	/**************************************************************************/
	/* 空きハンドル検索                                                       */
	/**************************************************************************/
	for( l_srchhandle = 0; l_srchhandle < DFMR_USR_MAXSEARCHHANDLE; l_srchhandle ++ )
	{
		if( tfmr_searchhandle[ l_srchhandle ].sectornum == 0xFFFFFFFF )
		{       /* ハンドルの検索開始セクタ番号が初期値なら空きハンドルとする */
			break;
		}
	}
	if( l_srchhandle >= DFMR_USR_MAXSEARCHHANDLE )  /* 空きハンドルなしの場合 */
	{
		/* 検索ハンドル用セマフォ資源返却 */
		l_ortn = FMR_osSigSem(
			g_semid
			);
		if( l_ortn != DFMR_OK )
		{
			l_rtn = l_ortn;
		}
		else
		{
			l_rtn = DFMR_EMFILE;           /* オープンファイルオーバー返却       */
		}
		goto ERROR;
	}
	/**************************************************************************/
	/* ファイル状態の設定                                                     */
	/**************************************************************************/
	FMR_setFileInfo(
		&l_entry,
		&l_getname[ 0 ],
		fileinfo
		);
	/**************************************************************************/
	/* 検索ハンドルの設定を行う                                               */
	/**************************************************************************/
	tfmr_searchhandle[ l_srchhandle ].drivenum = l_drvnum;
	FMR_memCopy(
		&tfmr_searchhandle[ l_srchhandle ].keyword[ 0 ],
		l_pathlen,
		DFMR_MAXFILESZ
		);
	if( l_bottompos.sectnum == 0 )
	{
		/**********************************************************************/
		/* 対象エントリのショートファイル名が検索文字列と一致した場合         */
		/**********************************************************************/
		tfmr_searchhandle[ l_srchhandle ].sectornum = l_toppos.sectnum;
		tfmr_searchhandle[ l_srchhandle ].position = l_toppos.slot;
	}
	else
	{
		/**********************************************************************/
		/* 対象エントリのロングファイル名が検索文字列と一致した場合           */
		/**********************************************************************/
		tfmr_searchhandle[ l_srchhandle ].sectornum = l_bottompos.sectnum;
		tfmr_searchhandle[ l_srchhandle ].position = l_bottompos.slot;
		
	}
	/**************************************************************************/
	/* 検索ハンドル用セマフォ資源返却                                         */
	/**************************************************************************/
	l_ortn = FMR_osSigSem(
		g_semid
		);
	if( l_ortn != DFMR_OK )
	{
		l_rtn = l_ortn;
		goto ERROR;
	}
	/**************************************************************************/
	/* セマフォ資源返却                                                       */
	/**************************************************************************/
	l_ortn = FMR_osSigSem(
		MFMR_SEMID( l_drvnum )
		);
	if( l_ortn != DFMR_OK )
	{
		return ( F_LONG )l_ortn;
	}
	/**************************************************************************/
	/* 検索ハンドル番号返却                                                   */
	/**************************************************************************/
	return l_srchhandle;

ERROR:
	/* 異常終了 */
	l_ortn = FMR_osSigSem(              /* セマフォ資源返却                   */
		MFMR_SEMID( l_drvnum )
		);
	if( l_ortn != DFMR_OK )
	{
		return ( F_LONG )l_ortn;
	}
	return ( F_LONG )l_rtn;

}
