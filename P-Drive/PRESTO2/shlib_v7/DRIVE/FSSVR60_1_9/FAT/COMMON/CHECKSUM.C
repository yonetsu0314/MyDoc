/*         ：                                                                 */
/* パラメタ：IN:  F_CHAR * ショートファイル名                                 */
/*         ：                                                                 */
/*         ：OUT: なし                                                        */
/*         ：                                                                 */
/*         ：                                                                 */
/* 戻 り 値：F_BYTE サム値                                                    */
/*         ：                                                                 */
/* 概    要：ショートファイル名からサム値を求める                             */
/*         ：                                                                 */
