#include "fsbase.h"

F_BYTE FMR_getChkSum(
    F_CHAR * filename                   /* ショートファイル名                 */
)
{
    F_BYTE l_cnt;                       /* ループカウンタ                     */
    F_BYTE l_sum;                       /* サム値                             */

    l_sum = 0;                          /* サム値←0                          */
    
    for ( l_cnt = 0; l_cnt < DFMR_FNAMESZ + DFMR_EXTSZ; l_cnt ++, filename ++ )
    {
        l_sum = ( F_BYTE )( ( (l_sum & 1) ? 0x80 : 0 ) + ( l_sum >> 1 ) + *( filename ) );
    }
    return( l_sum );
}
