#if	!defined(NZI005_H)
#define	NZI005_H

enum nzi0050e {
        LOG_DCS1	= 0x0001,  	/* 排出完了信号１		 */
        LOG_DCS2	= 0x0002,	/* 排出完了信号２		 */
        LOG_ERR1	= 0x0004,  	/* エラー信号１			 */
        LOG_ERR2	= 0x0008,	/* エラー信号２			 */
        LOG_IFD1	= 0x0010,  	/* 供給制御信号１		 */
        LOG_IFD2	= 0x0020,	/* 供給制御信号２		 */
        LOG_CTL1	= 0x0040,  	/* コントロール信号１		 */
        LOG_CTL2	= 0x0080,	/* コントロール信号２		 */
        LOG_ILK1	= 0x0100,  	/* 連動信号１			 */
        LOG_ILK2	= 0x0200,	/* 連動信号２			 */
        LOG_AUX1	= 0x0400,  	/* ＡＵＸ５（ＮＺの場合ＡＵＸ１） */
        LOG_AUX2	= 0x0800,	/* ＡＵＸ６（ＮＺの場合ＡＵＸ２） */
        LOG_AUX3	= 0x1000,  	/* ＡＵＸ１（ＮＺの場合ＡＵＸ３） */
        LOG_AUX4	= 0x2000,	/* ＡＵＸ２（ＮＺの場合ＡＵＸ４） */
        LOG_AUX5	= 0x4000,  	/* ＡＵＸ３（ＮＺの場合ＡＵＸ５） */
        LOG_AUX6	= (signed)0x8000,	/* ＡＵＸ４（ＮＺの場合ＡＵＸ６） */
};

/*------------------------------------------------------*/
/*	ロジック波形データの応答パラメータパケット	*/
/*							*/
/*------------------------------------------------------*/

typedef struct nzi005t {
	unsigned char	unit_no;	/* リレーユニット番号	*/
	unsigned char	nof_data;	/* データの数		*/
	enum nzi0050e	data[32];	/* データ		*/
} NZI005T;

#if	defined(STORAGE)
struct nzi005t	nzi005s;
#else
#if	!defined(NOEXTERN)
extern struct nzi005t	nzi005s;
#endif
#endif

#endif
