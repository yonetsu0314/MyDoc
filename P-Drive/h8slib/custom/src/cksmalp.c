unsigned char
cksum_alpha(
	register unsigned char	*buff,
	register int			len
	)
{
	unsigned char		sum = 0;

	for( ; len ; --len) {
		if(*buff & 0xe0)  sum += *buff;
		++buff;
	}
	return(sum);
}
