/* 引き数																*/
/*		*buff		: ﾊﾞｯﾌｧへのﾎﾟｲﾝﾀ									*/
/*		len			: ﾊﾞｲﾄ数											*/
/*																		*/
/* 戻り値																*/
/*		unsigned char	: 計算結果										*/
