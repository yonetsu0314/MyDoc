#include	<stddef.h>									/* ANSI std		*/
#include	<fcntl.h>
#include	<rom\handle.h>
#include	<drive\iofsys.h>

int
write(
	int		fd,
	char	*buff,
	int		len
	)
{
	int		ch, i;

	ch = fd & 0xff;
	if(((i= ((fd & 0xff00) >> 8)-1) < 0) || (i > __KEY_MAX_-1) ||
	  (iodrv_write[i] == NULL))						return(-1);
	return(iodrv_write[i](ch, buff, len));
}
